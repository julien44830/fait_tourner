import { useEffect, useState } from "react";
import { NavLink } from "react-router-dom"; // Assure-toi d'importer React
import BookHome from "../component/BookHome";

interface Book {
    id: number;
    name: string;
}

export default function Home() {
    // ‚úÖ useState doit √™tre dans le composant
    const [book, setBooks] = useState<Book[]>([]);
    const [name, setName] = useState<string | null>(null);

    useEffect(() => {
        setName(localStorage.getItem("name"));
        console.log("Token stock√© :", localStorage.getItem("token"));

        const fetchBooks = async () => {
            const token = localStorage.getItem("token"); // V√©rifie que le token est bien stock√©
            if (!token) {
                console.error("‚ùå Aucun token trouv√©, acc√®s refus√©.");
                return;
            }

            try {
                const response = await fetch(
                    "http://localhost:4000/api/books",
                    {
                        method: "GET",
                        headers: {
                            "Content-Type": "application/json",
                            Authorization: `Bearer ${token}`, // Ajout du token
                        },
                    }
                );

                if (!response.ok) {
                    throw new Error(`Erreur HTTP ${response.status}`);
                }

                const data = await response.json();
                console.log("%c‚ß≠", "color: #0088cc", data);
                setBooks(data);
            } catch (error) {
                console.error(
                    "‚ùå Erreur lors de la r√©cup√©ration des books :",
                    error
                );
            }
        };

        fetchBooks();
    }, []);

    if (!book) return <h1>Chargement...</h1>;
    console.log("%c‚ß≠", "color: #d90000", book);

    return (
        <div>
            <h2>Bonjour, {localStorage.getItem("name")}</h2>
            <br />
            <button>Cr√©er un nouveau book</button>
            <br />
            <h3>üìö Mes Books :</h3>
            {Array.isArray(book) && book.length > 0 ? (
                book.map((b) => (
                    <NavLink
                        key={b.id}
                        to={`/book/${b.id}`}
                    >
                        {b.name}
                    </NavLink>
                ))
            ) : (
                <p>‚ùå Vous n'avez aucun book actuellement</p>
            )}
        </div>
    );
}
